package model;

import java.util.Collection;
import java.util.Map;

public class State {
	private Collection<IPiece> useablePieces;
	private Map<IPosition, IPiece> pieceLocations;
	
	public Collection<IPiece> getUseablePieces() { return useablePieces; }
	public Map<IPosition, IPiece> getPieceLocations() { return pieceLocations; }
	
	public State(Collection<IPiece> useablePieces, Map<IPosition, IPiece> pieceLocations) {
		this.useablePieces = useablePieces;
		this.pieceLocations = pieceLocations;
	}
}
