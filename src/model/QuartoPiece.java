package model;

/**
 * A specific piece class for the game of Quarto. Functionally, all pieces in Quarto are the same, so we only
 * need one concrete class.
 * @author zpconn
 *
 */
public class QuartoPiece extends IPiece {
	// Quarto pieces are specified by length four bitstrings.
	private String type = "0000";
	
	public QuartoPiece(Class<?> idx) {
		super(idx);
	}

	@Override
	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}
}
